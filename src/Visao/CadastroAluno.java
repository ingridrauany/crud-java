    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        // TODO add your handling code here:
        String cpf = jTextFieldCPF.getText();
        String nome = jTextFieldNome.getText();
        String endereco = jTextFieldEndereco.getText();
        String data_nasc = jTextFielddtnasc.getText();

        Aluno aluno = new Aluno(cpf, nome, endereco, data_nasc);
        
        try {
            acessoAluno.InserirAluno(aluno);

        } catch (Exception e) {
            e.printStackTrace();
        }
        
        jTextFieldCPF.setText("");
        jTextFieldNome.setText("");
        jTextFieldEndereco.setText("");
        jTextFielddtnasc.setText("");
        
        JOptionPane.showMessageDialog(null, "Aluno Cadastrado");
        AulaQualidade.janela.setContentPane(AulaQualidade.CadastroAluno);
        AulaQualidade.janela.setVisible(true);
    }//GEN-LAST:event_jButtonSalvarActionPerformed

